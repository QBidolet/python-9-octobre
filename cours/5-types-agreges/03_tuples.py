# Un tuple est une liste immuable.

# un tuple vide
mon_tuple = ()
print(type(mon_tuple))

mon_tuple = (99, 12)
print(type(mon_tuple))

mon_tuple = 99, 12
print(type(mon_tuple))

# tuple d'un seul élément
mon_tuple = 99,
mon_tuple = (99,)
print(type(mon_tuple))

print('#' * 25)

