# Fonction print : pour afficher du texte à l'utilisateur dans le terminal.
print("Hello World!")

# Séparateur
print('Hello', "World!", sep="$")

# End
print("Hello", "World!", sep="$", end="**\n\n")

# Ecrire deux lignes avec un seul print avec le \n.
print("Première ligne.\nDeuxième ligne.")

# Ou alors avec un string multiligne.
print("""
Première ligne.
Deuxième ligne.
""")
